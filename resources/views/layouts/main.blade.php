						</div>
						`);
					});
					
					
				}
			});
	}
	function addWishlist(id) {
		
		if ($(".wish-list-heart"+id).hasClass("wishlist-liked")) {
			let user_id = $(".wish-list-heart"+id).data('user');
			let _token = $('meta[name="csrf-token"]').attr('content');
			$.ajax({
				type: "POST",
				url: `http://localhost:8080/BachKhoaAP/Jewelry_Shop/wishlist-del-api`,
				data: {
					id: id,
					_token: _token
				},
				success: function (data) {
					console.log("del success");
					$(".wish-list-heart"+id).removeClass("wishlist-liked");
					$("#wishlist-heart-icon"+id).addClass("fa-heart-o");
